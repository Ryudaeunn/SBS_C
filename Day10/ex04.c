#include <stdio.h>
void fune(int *a, int *b)
{
    *a = 100;
    *b = 200;
}
int main(){
    int a = 10, b =20;
    printf("func() 함수 호출 전\n");
    printf("a : %d\t b : %d\n",a,b);

/*  참조에 의한 전달 ( call by reference)
  : 변수의 값을 전달하는 게 아니라,
    해당 변수에 접근할 수 있는 주소값을 전달
*/         fune(&a ,&b);

    printf("func() 함수 호출 후\n");
    printf("a : %d\t b : %d\n",a,b);

    return 0;
}